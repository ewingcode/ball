public class HistoryData {
	private String date;
	private String date_name;
	private String action_date;
	private String date_class;
	private String gold;
	private String vgold;
	private String winloss;
	private String winloss_class;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getDate_name() {
		return date_name;
	}

	public void setDate_name(String date_name) {
		this.date_name = date_name;
	}

	public String getAction_date() {
		return action_date;
	}

	public void setAction_date(String action_date) {
		this.action_date = action_date;
	}

	public String getDate_class() {
		return date_class;
	}

	public void setDate_class(String date_class) {
		this.date_class = date_class;
	}

	public String getGold() {
		return gold;
	}

	public void setGold(String gold) {
		this.gold = gold;
	}

	public String getVgold() {
		return vgold;
	}

	public void setVgold(String vgold) {
		this.vgold = vgold;
	}

	public String getWinloss() {
		return winloss;
	}

	public void setWinloss(String winloss) {
		this.winloss = winloss;
	}

	public String getWinloss_class() {
		return winloss_class;
	}

	public void setWinloss_class(String winloss_class) {
		this.winloss_class = winloss_class;
	}

}
